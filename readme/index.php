    <div class="recap">
        <h1>Recap</h1>
     


        <div>
                    Étape 1 : Télécharger WampServer
Tout d'abord, vous devez télécharger le logiciel WampServer. Vous pouvez le faire en allant sur le site officiel de WampServer et en cliquant sur le bouton de téléchargement.

        </div>
        <div>
        Étape 2 : Exécuter le fichier d'installation
Une fois que le téléchargement est terminé, ouvrez le dossier de téléchargement et double-cliquez sur le fichier d'installation. Cela va lancer l'assistant d'installation de WampServer.

        </div>

        <div>
        Étape 3 : Accepter les conditions de licence
Lorsque vous lancez l'assistant d'installation, vous verrez les termes et conditions de licence. Lisez-les attentivement et cliquez sur le bouton "J'accepte" si vous êtes d'accord avec eux.

        </div>


        <div>
        Étape 4 : Sélectionner le répertoire d'installation
L'assistant d'installation vous demandera maintenant de sélectionner le répertoire dans lequel vous souhaitez installer WampServer. Vous pouvez utiliser le répertoire par défaut ou en choisir un autre si vous le souhaitez.

        </div>
        <div>
        Étape 5 : Sélectionner les composants à installer
L'assistant d'installation vous permet également de sélectionner les composants que vous souhaitez installer. Par défaut, tous les composants sont sélectionnés. Si vous ne savez pas quels composants vous devez installer, vous pouvez simplement laisser les paramètres par défaut.

        </div>

<div>
Étape 6 : Installer WampServer
Une fois que vous avez sélectionné les composants à installer, cliquez sur le bouton "Installer". L'assistant d'installation commencera à installer WampServer sur votre ordinateur.

</div>
<div>
    Étape 7 : Configuration de WampServer
Une fois l'installation terminée, vous verrez un écran de configuration de WampServer. Vous pouvez laisser les paramètres par défaut ou les modifier si vous le souhaitez. Cliquez sur "Terminer" pour terminer la configuration.

</div>
<div>
Et voilà, WampServer est maintenant installé sur votre ordinateur ! Vous pouvez maintenant l'ouvrir et commencer à l'utiliser pour créer des sites web.

</div>




